public class ShiftOperationDemo {
    public static void main(String[] args) {
        // 4
        System.out.println(8 >> 1);
        // 2
        System.out.println(8 >> 2);
        // 16
        System.out.println(8 << 1);
        // 32
        System.out.println(8 << 2);
    }
}
